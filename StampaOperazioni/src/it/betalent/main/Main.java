package it.betalent.main;

public class Main {
	
	public static void main (String[] args) {
		int numero1 = 10;
		int numero2 = 3;
		
		System.out.println("La somma di " + numero1 + " + " + numero2 + " e' : " + (numero1+numero2));
		System.out.println("Il modulo di " + numero1 + " % " + numero2 + " e' : " + (numero1%numero2));
		

	}

}
